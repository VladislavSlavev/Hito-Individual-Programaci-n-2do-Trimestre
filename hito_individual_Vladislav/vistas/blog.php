                        <h1 class="titulo">Diferencias entre lenguajes de programación <br> orientada a objetos, a eventos y lenguajes procedimentales.</h1>
                        <p class="texto-articulo">
                           <br>
                           Los lenguajes de programación se pueden clasificar en diferentes categorías según la forma en que se aborda la programación. Las tres categorías principales son la programación orientada a objetos, la programación orientada a eventos y la programación procedimental.<br> A continuación, se describen las principales diferencias entre estas categorías de programación: <br><br>

                           1. Programación procedimental: La programación procedimental se centra en la creación de procedimientos, también conocidos como funciones, que se utilizan para llevar a cabo tareas específicas en un programa. En este tipo de programación, el programa se divide en una serie de procedimientos que se ejecutan en un orden específico. Los lenguajes de programación procedimentales incluyen C, Pascal y Basic. <br><br>
                       
                           2. Programación orientada a objetos: La programación orientada a objetos (POO) se centra en la creación de objetos que tienen propiedades y métodos. Los objetos son instancias de una clase, que es una plantilla para la creación de objetos. Los métodos son las acciones que pueden llevar a cabo los objetos y las propiedades son las características que definen a cada objeto. La POO se utiliza comúnmente en lenguajes como Java, Python y C++. <br><br>
                       
                           3. Programación orientada a eventos: La programación orientada a eventos se centra en la creación de programas que responden a eventos específicos, como un clic del ratón o una pulsación de tecla. En este tipo de programación, se escriben funciones que se activan en respuesta a un evento en particular. Los lenguajes de programación orientados a eventos incluyen Visual Basic y JavaScript. <br><br>
                       
                       En resumen, la programación procedimental se centra en la creación de procedimientos, mientras que la programación orientada a objetos se centra en la creación de objetos que tienen propiedades y métodos. La programación orientada a eventos se centra en la creación de programas que responden a eventos específicos.
